    // BOTÃO DE CADASTRO
    private void buttonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_buttonConfirmarActionPerformed
        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String senha = new String(txtNovaSenha.getPassword());
        String confirmarSenha = new String(txtConfirmarSenha.getPassword());
        String avisoMensagem = "";
        String avisoTitulo = "";
        int tipoMensagem = javax.swing.JOptionPane.ERROR_MESSAGE;

        if (nome.isEmpty() || email.isEmpty() || senha.isEmpty() || confirmarSenha.isEmpty()) {
            avisoMensagem = "Por favor, preencha todos os campos!";
            avisoTitulo = "Campos Vazios";
            tipoMensagem = javax.swing.JOptionPane.WARNING_MESSAGE;

        } else if (!senha.equals(confirmarSenha)) {
            avisoMensagem = "As senhas não coincidem!";
            avisoTitulo = "Erro de Senha";
            tipoMensagem = javax.swing.JOptionPane.ERROR_MESSAGE;
        }

        if (!avisoMensagem.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    avisoMensagem,
                    avisoTitulo,
                    tipoMensagem);
        } else {
            //Monta a pessoa para mandar para o controller
            Pessoa novaPessoa = new Pessoa();
            novaPessoa.setNome(nome);
            novaPessoa.setEmail(email);
            novaPessoa.setSenha(senha);
            novaPessoa.setAdm(false); 
            
            //Chama o controller para registrar pessoa no banco de dados
            try{
                pessoaController.register(novaPessoa);
                
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Usuário registrado com sucesso!",
                        "Sucesso",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);
                // Voltar para a tela de login após registro
                buttonNewLoginActionPerformed(null);
            } catch (RuntimeException e){
                mensagem("aviso", e.getMessage());
            }
        }
    }// GEN-LAST:event_buttonConfirmarActionPerformed

